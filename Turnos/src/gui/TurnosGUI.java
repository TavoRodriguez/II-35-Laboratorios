    private void actualizarTablaTurnos() {
        DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        modelo.addColumn("Turnos");

        if (listaTurnos.estaVacia()) {
            modelo.addRow(new Object[]{"(lista vacía)"});
            TablaTurnos.setModel(modelo);
            turnoActualLabel.setText("Turno Actual: Ninguno");
            return;
        }

        for (Object turno : (desdeActual
                ? listaTurnos.obtenerDesdeActual()
                : listaTurnos.obtenerTodos(adelante))) {

            modelo.addRow(new Object[]{turno});
        }

        TablaTurnos.setModel(modelo);
        SwingUtilities.invokeLater(() -> enfocarTurnoEnTabla());
    }


    private void enfocarTurnoEnTabla() {
        String turnoActual = (String) listaTurnos.obtenerActual();
        if (turnoActual == null) {
            return;
        }

        for (int i = 0; i < TablaTurnos.getRowCount(); i++) {
            String valor = (String) TablaTurnos.getValueAt(i, 0);
            if (turnoActual.equals(valor)) {
                TablaTurnos.setRowSelectionInterval(i, i); // Selecciona fila
                TablaTurnos.scrollRectToVisible(TablaTurnos.getCellRect(i, 0, true)); // Hace scroll si es necesario
                break;
            }
        }
    }

