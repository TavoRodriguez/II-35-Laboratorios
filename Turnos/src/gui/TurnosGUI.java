    private void btnSeguirActualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeguirActualActionPerformed
        if (listaTurnos.estaVacia()) {
            JOptionPane.showMessageDialog(this, "No hay turnos en la lista.");
            return;
        }

        desdeActual = !desdeActual;

        actualizarTablaTurnos();

        btnSeguirActual.setIcon(new javax.swing.ImageIcon(
                getClass().getResource(desdeActual ? "/icons/loss.png" : "/icons/profits.png")));

        btnSeguirActual.setToolTipText(desdeActual
                ? "Mostrando desde el turno actual"
                : "Mostrar en orden normal");

        JOptionPane.showMessageDialog(this,
                desdeActual ? "Orden desde el turno actual." : "Orden normal de turnos.");
    }//GEN-LAST:event_btnSeguirActualActionPerformed

