    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "¿Deseas borrar todos los turnos?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            listaTurnos = new ListaCircular();
            CargadorDeTurnos.guardarEnArchivo(listaTurnos, "turnos");
            actualizarTablaTurnos();
            turnoActualLabel.setText("Turno Actual: Ninguno");
        }
    }//GEN-LAST:event_btnClearActionPerformed

    
    
    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed
        if (listaTurnos.estaVacia()) {
            JOptionPane.showMessageDialog(this, "No hay turnos en la lista.");
            return;
        }
        adelante = !adelante;
        actualizarTablaTurnos();

        btnOrder.setIcon(new javax.swing.ImageIcon(getClass().getResource(adelante ? "/icons/profits.png" : "/icons/loss.png")));
        btnOrder.setToolTipText(adelante ? "Orden normal" : "Orden inverso");

        String mensaje = adelante ? "Ordenado de inicio a fin." : "Ordenado de fin a inicio.";
        JOptionPane.showMessageDialog(this, mensaje);
    }//GEN-LAST:event_btnOrderActionPerformed

