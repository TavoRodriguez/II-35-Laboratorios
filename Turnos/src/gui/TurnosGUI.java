    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nuevoTurno = JOptionPane.showInputDialog(this, "Ingrese el nombre del turno:");
        if (nuevoTurno != null && !nuevoTurno.trim().isEmpty()) {
            listaTurnos.agregarTurno(nuevoTurno.trim());
            JOptionPane.showMessageDialog(this, "Turno agregado: " + nuevoTurno);

            // Guardar automáticamente
            CargadorDeTurnos.guardarEnArchivo(listaTurnos, "turnos");

            turnoActualLabel.setText("Turno Actual: " + listaTurnos.obtenerActual());
            actualizarTablaTurnos();
            enfocarTurnoEnTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre válido.");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed
    private void anteriorSiguiente(boolean siguiente) {
        if (listaTurnos.estaVacia()) {
            JOptionPane.showMessageDialog(this, "No hay turnos registrados.");
            return;
        }

        turnoActualLabel.setText("Turno Actual: "
                + (String) (siguiente ? listaTurnos.avanzar() : listaTurnos.retroceder()));

        enfocarTurnoEnTabla();
    }


